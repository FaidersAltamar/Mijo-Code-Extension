const MAP: Record<string, Record<string, string>> = { en: EN, es: ES };

let _currentLocale: string = "es";
try { _currentLocale = document.getElementById("root")?.dataset?.locale || "es"; } catch {}

const I18nContext = React.createContext<string>(_currentLocale);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = React.useState(_currentLocale);
  React.useEffect(() => {
    const h = (e: MessageEvent) => {
      if (e.data?.type === "setLocale") {
        _currentLocale = e.data.locale || "en";
        setLocale(_currentLocale);
      }
    };
    window.addEventListener("message", h);
    return () => window.removeEventListener("message", h);
  }, []);
  return <I18nContext.Provider value={locale}>{children}</I18nContext.Provider>;
}

export function t(key: string, vars?: Record<string, string | number>): string {
  const dict = MAP[_currentLocale] || EN;
  let val = dict[key] ?? EN[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) val = val.replace(`{${k}}`, String(v));
  return val;
}

export function useT() {
  const locale = React.useContext(I18nContext);
  return (key: string, vars?: Record<string, string | number>) => {
    const dict = MAP[locale] || EN;
    let val = dict[key] ?? EN[key] ?? key;
    if (vars) for (const [k, v] of Object.entries(vars)) val = val.replace(`{${k}}`, String(v));
    return val;
  };
}
